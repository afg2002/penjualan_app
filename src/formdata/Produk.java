 protected void datatable() {

  Object[] kolom = {"ID", "Nama Produk", "Stok","Harga", "ID Kategori","Nama Kategori"};
  
  tabmode = new DefaultTableModel(null, kolom);
  tProduk.setModel(tabmode);

  String sql = "SELECT id_produk, nama_produk, stok,harga, k.id_kategori,k.nama_kategori FROM produk p JOIN kategori_produk k ON p.id_kategori = k.id_kategori";

  try{
    Statement stat = conn.createStatement();
    ResultSet res = stat.executeQuery(sql);

    while(res.next()){

      String id = res.getString("id_produk"); 
      String nama = res.getString("nama_produk");
      String harga = res.getString("harga");
      String stok = res.getString("stok");
      String id_kategori = res.getString("id_kategori");
      String kategori = res.getString("nama_kategori");

      Object[] data = {id, nama, stok,harga, id_kategori,kategori};

      tabmode.addRow(data);
    }

  }catch(SQLException e){
    Logger.getLogger(Produk.class.getName()).log(Level.SEVERE, null, e); 
  }

}
 
 // Ambil data kategori
private void loadKategoriCombo() {

  cbKategori.removeAllItems();

  try {

    String sql = "SELECT id_kategori, nama_kategori FROM kategori_produk";
    
    Statement st = conn.createStatement();
    ResultSet rs = st.executeQuery(sql);

    while(rs.next()) {
      
      int id = rs.getInt("id_kategori");
      String nama = rs.getString("nama_kategori");
      
      cbKategori.addItem(nama);
      
    }
    
  } catch (SQLException e) {
    e.printStackTrace();
  }
  
}

// Ambil ID kategori berdasarkan nama terpilih
private void getIdKategori() {

  String nama = cbKategori.getSelectedItem().toString();
  
  try {

    String sql = "SELECT id_kategori FROM kategori_produk WHERE nama_kategori=?";
    
    PreparedStatement ps = conn.prepareStatement(sql);
    ps.setString(1, nama);
    
    ResultSet rs = ps.executeQuery();
    
    if(rs.next()) {
        kategoriId = rs.getInt("id_kategori");
    }
    
  } catch (SQLException e) {
    e.printStackTrace();
  }

  
}


