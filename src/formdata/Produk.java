    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
      // Ambil nilai input  
    int produkId = Integer.parseInt(txtIdProduk.getText());
    String namaProduk = txtNamaProduk.getText();   
    String harga = txtHarga.getText();
    String stok = txtStok.getText();
    String[] columns = new String[] {"nama_produk", "harga", "stok","id_kategori"};

    Object[] values = new Object[] {namaProduk, harga,stok, kategoriId};

    // Kondisi berdasarkan id produk   
    String condition = "id_produk = " + produkId;

    try {

      db.updateData(conn, "produk", columns, values, condition);

      datatable();
      reset.resetTextFields(rootPane);

    } catch (SQLException ex) {
      ex.printStackTrace();
    }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
       // Ambil nilai input dari form
        int idProduk = Integer.parseInt(txtIdProduk.getText());

        // Buat kondisi berdasarkan customer_id
        String condition = "id_produk = " + idProduk;

        try {
            // Panggil fungsi deleteData dengan menggunakan koneksi database
            db.deleteData(conn, "Produk", condition);
            datatable();
            // Reset form setelah delete berhasil
            reset.resetTextFields(rootPane);
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Gagal menghapus data");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

