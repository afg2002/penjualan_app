    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
    // Ambil nilai dari form
    String namaKategori = txtNamaKategori.getText(); 

    // Buat array kolom dan nilai
    String[] columns = {"id_kategori", "nama_kategori"};
    Object[] values = {"0", namaKategori};

    try{

      // Panggil fungsi insert  
      db.insertData(conn, "kategori_produk", columns, values);

      // Reset form
      reset.resetTextFields(rootPane);

      // Refresh tabel  
      datatable();

    }catch(SQLException e){

      JOptionPane.showMessageDialog(null, "Gagal menambah data");
      e.printStackTrace();

    }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
      // Ambil nilai input  
    int produkId = Integer.parseInt(txtIdKategori.getText());
    String namaProduk = txtNamaKategori.getText();   

    String[] columns = new String[] {"nama_produk", "id_kategori"};

    Object[] values = new Object[] {namaProduk,kategoriId};

    // Kondisi berdasarkan id produk   
    String condition = "id_produk = " + produkId;

    try {

      db.updateData(conn, "produk", columns, values, condition);

      datatable();
      reset.resetTextFields(rootPane);

    } catch (SQLException ex) {
      ex.printStackTrace();
    }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
       // Ambil nilai input dari form
        int idProduk = Integer.parseInt(txtIdKategori.getText());

        // Buat kondisi berdasarkan customer_id
        String condition = "id_produk = " + idProduk;

        try {
            // Panggil fungsi deleteData dengan menggunakan koneksi database
            db.deleteData(conn, "Produk", condition);
            datatable();
            // Reset form setelah delete berhasil
            reset.resetTextFields(rootPane);
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Gagal menghapus data");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

