 protected void datatable() {
Object[] kolom = {"ID Kategori", "Nama Kategori"};

tabmode = new DefaultTableModel(null, kolom);
tKategori.setModel(tabmode);

String sql = "SELECT id_kategori, nama_kategori FROM kategori_produk";

try{

  Statement stat = conn.createStatement();
  ResultSet res = stat.executeQuery(sql);

  while(res.next()){

    String idKategori = res.getString("id_kategori");
    String namaKategori = res.getString("nama_kategori");

    Object[] data = {idKategori, namaKategori};

    tabmode.addRow(data);

  }

}catch(SQLException e){
  Logger.getLogger(Kategori.class.getName()).log(Level.SEVERE, null, e);
}

}
 


